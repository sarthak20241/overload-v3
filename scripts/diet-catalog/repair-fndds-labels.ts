/**
 * One-off repair for FNDDS portion labels that shipped as numeric codes.
 *
 * Before the ingest fix (see ingest-usda.ts), FNDDS survey rows were labeled
 * `1 <modifier>` where modifier is the FNDDS portion CODE ("1 64556"), because
 * the ingester preferred `modifier` over `portion_description` unconditionally.
 * The human text lives in portion_description and already includes its own
 * quantity ("1 cup", "1 tablespoon"). Codes map 1:1 to descriptions globally,
 * so this repairs prod IN PLACE from the code->description dictionary in any
 * FNDDS food_portion.csv download; gram weights are untouched.
 *
 * What the emitted SQL does (single transaction):
 *   0. backs up every affected row into _fndds_label_backup_20260717
 *   1. deletes "Quantity not specified" (code 90000) rows - useless as anchors
 *   2. deletes code rows whose repaired label already exists on the same food
 *   3. rewrites the remaining `1 <code>` labels to the FNDDS description
 *   4. re-elects a default serving (lowest seq) for foods whose default was
 *      deleted in step 1/2
 *
 * Run:    npx tsx scripts/diet-catalog/repair-fndds-labels.ts <path-to-food_portion.csv>
 * Output: supabase/seed/fndds_label_repair.generated.sql
 * Revert: update public.food_servings s set label = b.label, is_default = b.is_default
 *         from _fndds_label_backup_20260717 b where s.id = b.id;  (deleted rows:
 *         re-insert from the backup table)
 */

import fs from 'node:fs';
import path from 'node:path';

const REPO_ROOT = path.resolve(__dirname, '../..');

function parseCsvLine(line: string): string[] {
  const out: string[] = [];
  let field = '', inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (inQuotes) {
      if (c === '"') { if (line[i + 1] === '"') { field += '"'; i++; } else inQuotes = false; }
      else field += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ',') { out.push(field); field = ''; }
    else field += c;
  }
  out.push(field);
  return out;
}

function main() {
  const csvPath = process.argv[2];
  if (!csvPath || !fs.existsSync(csvPath)) {
    console.error('Usage: npx tsx scripts/diet-catalog/repair-fndds-labels.ts <path-to-FNDDS-food_portion.csv>');
    process.exit(1);
  }

  const lines = fs.readFileSync(csvPath, 'utf8').split('\n');
  const h = parseCsvLine(lines[0]);
  const iDesc = h.indexOf('portion_description'), iMod = h.indexOf('modifier');

  // code -> description; verified 1:1 in the FNDDS export (a code is a global
  // FNDDS measure id, not per-food).
  const map = new Map<string, string>();
  for (let i = 1; i < lines.length; i++) {
    if (!lines[i]) continue;
    const r = parseCsvLine(lines[i]);
    const code = (r[iMod] || '').trim();
    const desc = (r[iDesc] || '').trim();
    if (!/^\d{4,6}$/.test(code) || !desc) continue;
    if (/quantity not specified/i.test(desc)) continue;
    const prev = map.get(code);
    if (prev && prev !== desc) throw new Error(`code ${code} maps to both "${prev}" and "${desc}"`);
    map.set(code, desc);
  }
  if (map.size === 0) throw new Error('no code->description pairs found; wrong csv?');

  const esc = (s: string) => s.replace(/'/g, "''");
  const values = [...map.entries()]
    .map(([code, desc]) => `('${code}','${esc(desc)}')`)
    .join(',\n');

  const sql = `-- GENERATED by scripts/diet-catalog/repair-fndds-labels.ts - do not edit by hand.
-- Repairs FNDDS portion labels stored as codes ("1 64556" -> "1 tablespoon").
-- ${map.size} code->description pairs. Idempotent: rerunning matches nothing.
begin;

-- Back up the WHOLE row (select *), not a hand-picked subset: steps 1-2 DELETE
-- rows, and a partial backup (missing source and any other columns) could not
-- re-insert them faithfully on a revert.
create table if not exists public._fndds_label_backup_20260717 as
select * from public.food_servings
where label ~ '^1\\s+\\d{4,6}$';

-- 1. drop "Quantity not specified" rows (code 90000): no portion information.
delete from public.food_servings where label ~ '^1\\s+90000$';

-- 2. drop code rows whose repaired label already exists on the same food
--    (e.g. the food also got a real "1 cup" from an SR/OFF merge).
with map(code, descr) as (values
${values}
)
delete from public.food_servings s
using map m
where s.label = '1 ' || m.code
  and exists (
    select 1 from public.food_servings s2
    where s2.food_id = s.food_id and s2.id <> s.id
      and lower(s2.label) = lower(m.descr)
  );

-- 3. rewrite the remaining code labels to the FNDDS description.
with map(code, descr) as (values
${values}
)
update public.food_servings s
set label = m.descr
from map m
where s.label = '1 ' || m.code;

-- 4. foods left with no default (their default was deleted above): promote the
--    lowest-seq remaining serving. Scoped to foods THIS repair touched (in the
--    backup) - otherwise it would stamp a default onto every catalog food that
--    deliberately has none, e.g. a user-created or OFF-imported food.
update public.food_servings s
set is_default = true
from (
  select distinct on (food_id) id
  from public.food_servings
  where food_id in (select distinct food_id from public._fndds_label_backup_20260717)
    and food_id in (
      select food_id from public.food_servings
      group by food_id
      having not bool_or(is_default)
    )
  order by food_id, seq, id
) pick
where s.id = pick.id;

commit;
`;

  const dest = path.join(REPO_ROOT, 'supabase/seed/fndds_label_repair.generated.sql');
  fs.mkdirSync(path.dirname(dest), { recursive: true });
  fs.writeFileSync(dest, sql);
  console.log(`[repair] ${map.size} codes -> ${path.relative(REPO_ROOT, dest)} (${(sql.length / 1e3).toFixed(0)} KB)`);
}

main();
